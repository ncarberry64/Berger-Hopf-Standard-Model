    required = [m4.PRIMARY_RESULT, "BHSM_ROUND_EQUATORIAL_M4_GEOMETRY_DERIVED", "BHSM_M4_GAUSS_CODAZZI_REDUCTION_DERIVED", "BHSM_NO_MONOPOLE_FIREWALL_DERIVED", "BHSM_FERMIONIC_CLIFFORD_BOUNDARY_READINESS_DERIVED", "FULL_BHSM_NOT_COMPLETE"]
