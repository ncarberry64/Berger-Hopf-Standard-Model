def load(key):
    return json.loads((ARTIFACTS / m4.ARTIFACT_FILES[key]).read_text(encoding="utf-8"))


def public_text():
    paths = [DOC, ROOT / "STATUS.md", ROOT / "CLAIMS.md", ROOT / "ARTIFACT_INDEX.md", ROOT / "CLI_REFERENCE.md"]
    paths.extend(ARTIFACTS / name for name in m4.ARTIFACT_FILES.values())
    return "\n".join(path.read_text(encoding="utf-8") for path in paths)


def test_twenty_six_deterministic_claim_safe_artifacts():
    assert len(m4.ARTIFACT_FILES) == 26
    assert len(set(m4.ARTIFACT_FILES.values())) == 26
    built = m4.build_artifact_payloads(ROOT)
    for key, filename in m4.ARTIFACT_FILES.items():
        payload = load(key)
        assert payload["primary_result"] == m4.PRIMARY_RESULT, key
        assert payload["v6_1_bosonic_results_preserved"] is True, key
        assert payload["physical_fermion_equation_assumed"] is False, key
        assert payload["magnetic_monopole_sector_used"] is False, key
        assert payload["measured_input_used"] is False, key
        assert payload["frozen_predictions_changed"] is False, key
        assert (ARTIFACTS / filename).read_text(encoding="utf-8") == m4.deterministic_json(built[key])


